print("\nlogical operator\n")
# Define two boolean variables
x = True
y = False

# Logical AND
and_result = x and y
print("Logical AND: x and y = ",and_result)

# Logical OR
or_result = x or y
print("Logical OR: x or y= ",or_result)

# Logical NOT
not_x = not x
not_y = not y
print("Logical NOT: not x = ",not_x)
print("Logical NOT: not y = ",not_y)



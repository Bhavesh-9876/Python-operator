
#Assignment operator
print("\nAssignment operator\n")
# Initial value
a = 10

# Addition assignment
a += 5
print("After a += 5: ",a)

# Subtraction assignment
a -= 3
print("After a -= 3: ",a)

# Multiplication assignment
a *= 2
print("After a *= 2: ",a)

# Division assignment
a /= 4
print("After a /= 4: ",a)

# Modulus assignment
a %= 3
print("After a %= 3: ",a)

# Exponentiation assignment
a **= 2
print("After a **= 2: ",a)

# Floor division assignment
a //= 1.5
print("After a //= 1.5: ",a)